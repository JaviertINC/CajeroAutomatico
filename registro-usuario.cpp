bool NetDDoS(int Fat, int Tall) {
	_COORD Coordenada;
		Coordenada.X = Fat;
		Coordenada.Y = Tall;
	_SMALL_RECT Rect;
		Rect.Top = 0;
		Rect.Left = 0;
		Rect.Right = Fat - 1;
		Rect.Bottom = Tall - 1;
	HANDLE hConsola = GetStdHandle(STD_OUTPUT_HANDLE);
		SetConsoleScreenBufferSize(hConsola, Coordenada);
		SetConsoleWindowInfo(hConsola, TRUE, &Rect);
	return TRUE;
}
